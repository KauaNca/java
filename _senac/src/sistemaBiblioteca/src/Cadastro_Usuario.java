    public Cadastro_Usuario(int id) {
        initComponents();
        id_usuario = id;

        Connection con = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;

        try {
            con = Conexao.conexao();

            // Consulta para recuperar dados com base no id_usuario
            String SQL = "SELECT nome, idade, cpf_cnpj, email, telefone, lo.usuario, senha "
                    + "FROM pessoa "
                    + "INNER JOIN usuario ON usuario.id_pessoa = pessoa.id_pessoa "
                    + "INNER JOIN login lo ON usuario.id_usuario = lo.id_usuario "
                    + "WHERE usuario.id_usuario = ?;";

            stmt = con.prepareStatement(SQL);

            // Defina o valor para o parâmetro id_usuario
            stmt.setInt(1, id_usuario); // Use setInt se id_usuario for um inteiro

            rs = stmt.executeQuery();
            if (rs.next()) {
                nome.setText(rs.getString("nome"));
                idade.setText(rs.getString("idade"));
                cpf_cnpj.setText(rs.getString("cpf_cnpj"));
                email.setText(rs.getString("email"));
                telefone.setText(rs.getString("telefone"));
                nomeUsuario.setText(rs.getString("usuario"));
                senha.setText(rs.getString("senha"));
            } else {
                System.out.println("ID NÃO ENCONTRADO");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (stmt != null) {
                    stmt.close();
                }
                if (con != null) {
                    con.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

    }

