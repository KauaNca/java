    private void btAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarActionPerformed
        Connection con = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;
        PreparedStatement stmt2 = null;

        try {
            con = Conexao.conexao();
            String comandoSQL = "UPDATE pessoa " +
                    "INNER JOIN usuario ON usuario.id_pessoa = pessoa.id_pessoa " +
                    "SET pessoa.nome = ?, pessoa.idade = ?, pessoa.cpf_cnpj = ?, pessoa.email = ?, pessoa.telefone = ? " +
                    "WHERE usuario.id_usuario = ?;";


            stmt = con.prepareStatement(comandoSQL);
            stmt.setString(1, nome.getText());
            stmt.setInt(2, Integer.parseInt(idade.getText()));
            stmt.setString(3, cpf_cnpj.getText());
            stmt.setString(4, email.getText());
            stmt.setString(5, telefone.getText());
            stmt.setInt(6, id_usuario);
            // Atualização na tabela login
            String sqlLogin = "UPDATE login SET usuario = ?,senha = ? WHERE id_usuario = ?";
            stmt2 = con.prepareStatement(sqlLogin);
            stmt2.setString(1, nomeUsuario.getText());
            stmt2.setString(2, senha.getText());
            stmt2.setInt(3, id_usuario);

            // Executar ambas as atualizações
            stmt.executeUpdate();
            stmt2.executeUpdate();
            stmt.executeUpdate();

        } catch (SQLException e) {
               System.out.println("ERRO AO ATUALIZAR OS DADOS" + e.getMessage());
        }
    }//GEN-LAST:event_btAtualizarActionPerformed

